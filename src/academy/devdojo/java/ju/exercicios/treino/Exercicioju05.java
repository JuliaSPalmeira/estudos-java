package academy.devdojo.java.ju.exercicios.treino;

public class Exercicioju05 {
    public static void main(String[] args) {
        int contador = 0;
        System.out.println("valor contador inicial: " +contador);

        contador +=5;
        System.out.println("soma 5 : "+contador);

        contador -= 2;
        System.out.println("menos 2 : "+contador);

        contador *=3;
        System.out.println("mult por 3 : "+contador);

        contador /=2;
        System.out.println("divide por 2 : "+contador);

        contador %=4;
        System.out.println("resto por 4: "+contador);

    }
}
